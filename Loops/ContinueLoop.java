import java.util.*;
public class ContinueLoop {
    public static void main(String[] args) {
        
        for(int i =1; i < 10; i++ ){
            if(i == 5){
                continue;    // for skipping the number or any thing and continue other
            }                // part we can used CONTINUE loop...
            System.out.println(i);
        }
    }
}
