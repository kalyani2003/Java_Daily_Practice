import java.util.*;
public class RelationOp {
    public static void main(String[] args) {
        
         int a =10;
         int b = 30;
         
         System.out.println(a==b); // false cond
         int c = 30;
         System.out.println(c==b);  // true cond

         System.out.println(a!=b); // true cond .....not operator
                                                
         System.out.println(a>b); // comparison

         System.out.println(a<b);  // comparison 
         
         System.out.println(a>=b);

         System.out.println(a<=b);
    }
}
